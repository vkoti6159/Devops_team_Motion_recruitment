'''
Assignment oprarators :  it is used to assign the right fand side values to left hand side variables 
Assignment operators : =, +=,-=,*=,/=,%=
'''
a=2
print(a)
a+=5       #which means a=a+5
print(a)
a-=1
print(a)
a*=2
print(a)
a/=2
print(a)
a%=1      #stores the remainder into  left hand side variable
print(a)